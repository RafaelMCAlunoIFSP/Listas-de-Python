codigo = int(input("Digite o código de acesso a um curso: "))

if (codigo >= 6 or codigo <= 0):

    print("Código inválido.")

elif (codigo == 1):

    print("Engenharia.")

elif (codigo == 2):

    print("Edificações.")

elif (codigo == 3):

    print("Sistemas Elétricos.")

elif (codigo == 4):

    print("Turismo.")

elif (codigo == 5):

    print("Análise de Sistemas.")